package oop;

public class Program {

    public static double maximum(double a, double b) {
        if(a > b) {
            return a;
        }
        else {
            return b;
        }
    }

    public static int maximum(int a, int b) {
        if(a > b) {
            return a;
        }
        else {
            return b;
        }
    }
}
